"""Player finishing & breakdown patterns: which rally lead-ups make a player go for a
winner vs. err and lose the point.

Deliberately built on the two things our data supports well — the **shot-type/zone
tokens** (from `shot_language`) and the **charted point outcome** (winner `*` / unforced
`@` / forced `#`) — not precise placement. For each player, given the K shots *before*
one of their strokes, how often is that stroke a winner vs an unforced error, relative to
their own baseline. So: which sequences are their green light (finish) and which are their
trouble (break down).

Run:  python experiments/shot_patterns/run.py
Writes reports/shot_patterns.md (marquee players) + reports/shot_patterns.csv (all).
"""

import sys
from collections import defaultdict
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
sys.path.insert(0, str(Path(__file__).resolve().parents[1] / "shot_language"))
sys.path.insert(0, str(Path(__file__).resolve().parents[2] / "src"))

import pandas as pd  # noqa: E402

from tokens import point_tokens, pretty  # noqa: E402
from match_charting_project.analysis.coverage import connect  # noqa: E402
from match_charting_project.paths import PROJECT_ROOT  # noqa: E402
from match_charting_project.shots.notation import parse_point  # noqa: E402

K = 2               # context = the K shots before the player's stroke
MIN_SHOTS = 4000    # a player needs this many contextful strokes to be ranked
MIN_CTX = 60        # a context needs this many of the player's strokes
MIN_EVENTS = 10     # ...and this many winners (or errors) for that context to rank
TOP = 4
GLABEL = {"M": "Men", "W": "Women"}
MARQUEE = {
    "M": ["Roger Federer", "Novak Djokovic", "Rafael Nadal", "Pete Sampras", "Andre Agassi"],
    "W": ["Serena Williams", "Iga Swiatek", "Martina Navratilova", "Steffi Graf"],
}


def collect(con, gender: str) -> dict:
    """player -> {n, w, e, f, ctx:{context: [n, winners, unforced, forced]}}."""
    acc: dict = defaultdict(lambda: {"n": 0, "w": 0, "e": 0, "f": 0,
                                     "ctx": defaultdict(lambda: [0, 0, 0, 0])})
    sql = (
        "SELECT m.player1, m.player2, p.svr, p.first_serve, p.second_serve, p.pt_winner "
        "FROM points p JOIN matches m USING (match_id) "
        "WHERE p.svr IN (1,2) AND p.pt_winner IN (1,2) AND m.gender = ?"
    )
    cur = con.execute(sql, [gender])
    while True:
        batch = cur.fetchmany(100_000)
        if not batch:
            break
        for p1, p2, svr, fs, ss, win in batch:
            pt = parse_point(fs, ss, svr, win)
            if not pt.parse_ok or len(pt.shots) <= K:
                continue
            toks = point_tokens(pt)
            names = {1: p1, 2: p2}
            for i in range(K, len(pt.shots)):
                a = acc[names[pt.shots[i].hitter]]
                a["n"] += 1
                c = a["ctx"][tuple(toks[i - K:i])]
                c[0] += 1
                term = pt.shots[i].terminal
                if term == "*":
                    a["w"] += 1
                    c[1] += 1
                elif term == "@":
                    a["e"] += 1
                    c[2] += 1
                elif term == "#":
                    a["f"] += 1
                    c[3] += 1
    return acc


def rank(a: dict, idx: int):
    """Top contexts by rate for outcome ``idx`` (1=winner, 2=unforced), with lift."""
    base = {1: a["w"], 2: a["e"]}[idx] / a["n"]
    rows = []
    for ctx, c in a["ctx"].items():
        if c[0] >= MIN_CTX and c[idx] >= MIN_EVENTS:
            rate = c[idx] / c[0]
            rows.append((rate, rate / base if base else 0.0, c[0], ctx))
    rows.sort(reverse=True)
    return base, rows


def _ctx_str(ctx) -> str:
    return " · ".join(pretty(t) for t in ctx)


def main() -> None:
    con = connect(read_only=True)
    md = ["# Player finishing & breakdown patterns", ""]
    md.append("*Generated by `experiments/shot_patterns/run.py`. For each player, the rally "
              "context (the two shots before their stroke) that most raises their chance of "
              "hitting a **winner** (green light) or an **unforced error** (trouble), vs their "
              "own baseline. Coarse shot-type/zone tokens + charted outcome only — no precise "
              "placement.*")
    md.append("")
    csv_rows = []
    for g in ("M", "W"):
        acc = collect(con, g)
        for player, a in acc.items():
            if a["n"] < MIN_SHOTS:
                continue
            wbase, wr = rank(a, 1)
            ebase, er = rank(a, 2)
            for rate, lift, n, ctx in wr:
                csv_rows.append({"player": player, "gender": g, "outcome": "winner",
                                 "context": _ctx_str(ctx), "n": n, "rate": round(rate, 3),
                                 "baseline": round(wbase, 3), "lift": round(lift, 2)})
            for rate, lift, n, ctx in er:
                csv_rows.append({"player": player, "gender": g, "outcome": "unforced_error",
                                 "context": _ctx_str(ctx), "n": n, "rate": round(rate, 3),
                                 "baseline": round(ebase, 3), "lift": round(lift, 2)})

        md.append(f"## {GLABEL[g]}\n")
        for player in MARQUEE[g]:
            a = acc.get(player)
            if not a or a["n"] < MIN_SHOTS:
                continue
            wbase, wr = rank(a, 1)
            ebase, er = rank(a, 2)
            md.append(f"### {player}")
            md.append(f"*baseline: winner {wbase:.1%} · unforced {ebase:.1%} of strokes; "
                      f"{a['n']:,} strokes analyzed*\n")
            md.append("**Green light** (lead-ups → most likely to hit a winner):")
            for rate, lift, n, ctx in wr[:TOP]:
                md.append(f"- `{_ctx_str(ctx)}` → **{rate:.0%}** winner ({lift:.1f}×, n={n})")
            md.append("\n**Trouble** (lead-ups → most likely to miss / unforced error):")
            for rate, lift, n, ctx in er[:TOP]:
                md.append(f"- `{_ctx_str(ctx)}` → **{rate:.0%}** error ({lift:.1f}×, n={n})")
            md.append("")
    con.close()

    pd.DataFrame(csv_rows).to_csv(PROJECT_ROOT / "reports" / "shot_patterns.csv", index=False)
    (PROJECT_ROOT / "reports" / "shot_patterns.md").write_text("\n".join(md))
    print(f"wrote reports/shot_patterns.md + shot_patterns.csv ({len(csv_rows)} player-context rows)")


if __name__ == "__main__":
    main()
