"""Career splitting — does it capture real change? (the justification for `player_eras`)

Run:  python experiments/career_splits/run.py

This is the *investigation* behind the optional `player_eras` DB table (built by
`match-charting-project eras`; logic in `match_charting_project.analysis.career_eras`).
It re-derives the evolve test, shows the chronological-vs-noise evidence and the
threshold sensitivity, and writes the report + figure. The era mapping itself now
lives in the DB table, not in this folder.
"""

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[2] / "src"))

import matplotlib  # noqa: E402
matplotlib.use("Agg")
import matplotlib.pyplot as plt  # noqa: E402
import numpy as np  # noqa: E402

from match_charting_project.analysis.career_eras import (  # noqa: E402
    FEATURES, MIN_ERA, MIN_CHARTED_YEARS, MIN_SPAN_YEARS, NOISE_RATIO,
    evaluate, greedy_k, wz)
from match_charting_project.analysis.coverage import connect  # noqa: E402
from match_charting_project.paths import PROJECT_ROOT  # noqa: E402

GLABEL = {"M": "Men", "W": "Women"}
CUTOFFS = [1.25, 1.5, 1.75, 2.0, 2.5]


def between_player(res, n_pairs: int = 2000) -> float:
    """Median style distance between two random *different* players' whole careers."""
    rng = np.random.default_rng(0)
    means = {n: wz(P, P["years"], res["mu"], res["sd"]) for n, P in res["eligible"].items()}
    names = list(means)
    d = [np.linalg.norm(means[a] - means[b])
         for a, b in (rng.choice(names, 2, replace=False) for _ in range(n_pairs))]
    return float(np.median(d))


def top_changed_features(P, mu, sd) -> str:
    a, b = greedy_k(P, 2)
    diff = wz(P, b, mu, sd) - wz(P, a, mu, sd)
    order = np.argsort(-np.abs(diff))[:3]
    return ", ".join(f"{FEATURES[i]} {'+' if diff[i] > 0 else '−'}{abs(diff[i]):.1f}σ" for i in order)


def fig(results, bp, path):
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))
    ax = axes[0]
    for g, c in (("M", "#1f77b4"), ("W", "#d62728")):
        tot = [P["total"] for P in results[g]["eligible"].values()]
        ax.hist(np.log10(tot), bins=30, alpha=0.5, label=GLABEL[g], color=c)
    for t in (MIN_ERA, 2 * MIN_ERA, 3 * MIN_ERA):
        ax.axvline(np.log10(t), color="gray", lw=0.8, ls=":")
    ax.set_xlabel("career charted points (log10)")
    ax.set_ylabel("players")
    ax.set_title("Career sizes (dotted: 1·, 2·, 3· era threshold)")
    ax.legend()
    ax = axes[1]
    dc = [r[1] for g in ("M", "W") for r in results[g]["rows"]]
    dr = [r[2] for g in ("M", "W") for r in results[g]["rows"]]
    bins = np.linspace(0, max(dc + dr), 30)
    ax.hist(dr, bins=bins, alpha=0.55, color="gray", label="random halves (noise)")
    ax.hist(dc, bins=bins, alpha=0.55, color="#ff7f0e", label="chronological (early vs late)")
    ax.axvline(bp, color="black", lw=1, ls="--", label="two different players")
    ax.set_xlabel("style distance (z-space)")
    ax.set_ylabel("splittable players")
    ax.set_title("Is an era change real? — mostly modest, with an evolving tail")
    ax.legend(fontsize=8)
    fig.tight_layout()
    fig.savefig(path, dpi=110)
    plt.close(fig)


def main():
    con = connect(read_only=True)
    results = {g: evaluate(con, g) for g in ("M", "W")}
    con.close()
    bp = {g: between_player(results[g]) for g in ("M", "W")}

    (PROJECT_ROOT / "reports" / "figures").mkdir(parents=True, exist_ok=True)
    fig(results, np.median(list(bp.values())), PROJECT_ROOT / "reports" / "figures" / "career_splits.png")

    base = sum(len(results[g]["eligible"]) for g in ("M", "W"))
    md = ["# Career splitting — does it capture real change?", ""]
    md.append("*Generated by `experiments/career_splits/run.py`. The justification for the "
              "optional **`player_eras`** table (build it with `match-charting-project eras`). "
              "Distances are in standardized style space (10 features); the split decision is "
              "deterministic (each player's noise estimate is seeded from their name).*")
    md.append("")
    for g in ("M", "W"):
        r = results[g]
        rows = r["rows"]
        ratios = np.array([x[3] for x in rows])
        dc = np.array([x[1] for x in rows])
        evolved = {x[0] for x in rows if x[3] > NOISE_RATIO}
        md.append(f"## {GLABEL[g]}")
        md.append("")
        md.append(f"- **Tracked players (≥{MIN_ERA} pts):** {len(r['eligible'])}; long-career "
                  f"candidates (≥{2*MIN_ERA} pts, ≥{MIN_SPAN_YEARS}-yr span, ≥{MIN_CHARTED_YEARS} "
                  f"charted yrs): {len(r['splittable'])}.")
        md.append(f"- **Is the change real?** median early-vs-late gap = {np.median(dc):.2f}, a "
                  f"median **{np.median(ratios):.2f}×** the random-split noise of the same data; "
                  f"**{len(evolved)}** ({len(evolved)/max(len(rows),1):.0%}) clear the {NOISE_RATIO}× "
                  "bar and are split.")
        md.append(f"- Two *different* players sit {bp[g]:.2f} apart, so a typical era gap is "
                  f"{np.median(dc)/bp[g]:.0%} of a whole different player.")
        md.append("")
        md.append("Most-evolved long careers (early→late, biggest feature shifts):")
        for n, d, _dr, _ratio, P in sorted(rows, key=lambda x: -x[1])[:6]:
            md.append(f"- **{n}** ({P['years'][0]}–{P['years'][-1]}, gap {d:.2f}): "
                      f"{top_changed_features(P, r['mu'], r['sd'])}")
        md.append("")

    # Threshold sensitivity — how the split count / entity count moves with the cutoff.
    md.append("## Threshold sensitivity")
    md.append("")
    md.append(f"How many careers split (and total entities, from {base} tracked) as the "
              "noise-ratio cutoff varies — the 1.5–2.0 band is crowded, and a uniform 2.0 "
              "erases the women (their sparser charting raises the noise floor):")
    md.append("")
    md.append("| cutoff | split (M / W) | entities |")
    md.append("|---|---|---|")
    for c in CUTOFFS:
        m = sum(1 for x in results["M"]["rows"] if x[3] > c)
        w = sum(1 for x in results["W"]["rows"] if x[3] > c)
        flag = "  ← used" if c == NOISE_RATIO else ""
        md.append(f"| >{c}{flag} | {m} / {w} | {base + m + w} |")
    md.append("")
    md.append("![career splits](figures/career_splits.png)")
    md.append("")
    pooled = np.median([x[3] for g in ("M", "W") for x in results[g]["rows"]])
    n_evolved = sum(len({x[0] for x in results[g]["rows"] if x[3] > NOISE_RATIO}) for g in ("M", "W"))
    md.append("## Verdict: split selectively, not across the board")
    md.append("")
    md.append(f"Even among long careers, the median early-vs-late style gap is only "
              f"**{pooled:.2f}×** the random-split noise — most players are stylistically "
              "*stable*, so a blanket split would dilute data. But a real minority — "
              f"**{n_evolved} players** — evolve clearly (>{NOISE_RATIO}× noise), with face-valid "
              "detections (Sabalenka's serve-yips fix, Paire's decline, Chang adding serve). "
              "Those are split binary early/late — the only contrast this test validated — and "
              "materialized into the **`player_eras`** table (everyone else stays whole), ready "
              "for the clustering / WPA / win-prob experiments and the web frontend to join to.")
    md.append("")
    (PROJECT_ROOT / "reports" / "career_splits.md").write_text("\n".join(md))

    for g in ("M", "W"):
        ratios = np.array([x[3] for x in results[g]["rows"]])
        print(f"[{g}] candidates {len(results[g]['splittable'])}, median ratio "
              f"{np.median(ratios):.2f}, split {(ratios>NOISE_RATIO).sum()}")
    print("wrote reports/career_splits.md + figure (era map -> `player_eras` table)")


if __name__ == "__main__":
    main()
